def get_QFT_gate(dim):
    """Generate QFT gate."""
    gate_Hd = np.zeros((dim, dim), dtype=complex)
    om_d = np.exp(1j*2*np.pi/dim)

    for j in range(dim):
        for k in range(dim):
            gate_Hd[j, k] = om_d**(j*k) / np.sqrt(dim)

    return gate_Hd


def test_example_qft(tmp_path, request):
    """Test 3-qubit QFT gate optimization using new Python interface."""
    datadir_path = os.path.join(tmp_path, request.node.name)

    # Parameters for 3-qubit case
    nqubits = 3
    freq01_all = [5.18, 5.12, 5.06, 5.0, 4.94]
    Jkl_coupling = 5e-3

    # Set up frequency vector
    freq01 = []
    Ne = []
    Ng = []
    for i in range(nqubits):
        freq01.append(freq01_all[len(freq01_all)-i-1])
        Ne.append(2)
        Ng.append(0)

    # Set up qubit CHAIN coupling
    Jkl = []
    for i in range(len(Ne)):
        for j in range(i+1, len(Ne)):
            if j == i+1:
                val = Jkl_coupling
            else:
                val = 0.0
            Jkl.append(val)

    T = 100.0
    favg = sum(freq01)/len(freq01)
    rotfreq = favg*np.ones(len(freq01))
    unitary = get_QFT_gate(np.prod(Ne))

    Pmin = 150*2
    spline_knot_spacing = 3.0
    maxctrl_MHz = 25.0
    cw_amp_thres = 5e-2
    cw_prox_thres = 1e-3
    gamma_energy = 1e-4
    gamma_tik0 = 1e-3
    gamma_dpdm = 0.0

    n_osc = 3
    n_levels = 8

    setup = create_config(
        nessential=Ne,
        nguard=Ng,
        transition_frequency=freq01,
        dipole_coupling=Jkl,
        rotation_frequency=list(rotfreq),
        control_amplitude_bound=[maxctrl_MHz/1000]*nqubits,
        total_time=T,
        Pmin=Pmin,
        spline_knot_spacing=spline_knot_spacing,
        control_zero_boundary_condition=False,  # Old default: new default is True
        output_directory=datadir_path,
    )

    # Recompute carrier frequencies with custom thresholds to match old test
    Hsys, Hc_re, Hc_im = hamiltonians(
        nessential=Ne,
        nguard=Ng,
        transition_frequency=freq01,
        crosskerr_coupling=[],
        dipole_coupling=Jkl,
        rotation_frequency=list(rotfreq),
    )
    carrier_freq, _ = get_resonances(
        nessential=Ne,
        nguard=Ng,
        Hsys=Hsys,
        Hc_re=Hc_re,
        Hc_im=Hc_im,
        rotation_frequency=list(rotfreq),
        cw_amp_thres=cw_amp_thres,
        cw_prox_thres=cw_prox_thres,
    )
    setup.carrier_frequencies = carrier_freq

    # Match old interface defaults
    setup.rand_seed = 1234
    setup.optim_tol_final_cost = 1e-4
    setup.optim_penalty_leakage = 0.1
    setup.output_optimization_stride = 1
    setup.linearsolver_maxiter = 20
    setup.optim_penalty_energy = gamma_energy   # 1e-4 (explicitly set in old test)
    setup.optim_tikhonov_coeff = gamma_tik0     # 1e-3
    setup.optim_penalty_dpdm = gamma_dpdm       # 0.0 (explicitly set in old test)
    setup.optim_penalty_variation = 0.01        # old default (new default is 0.0)
    setup.optim_maxiter = 10                    # maxiter=10

    # Match old per-oscillator initialization amplitudes:
    # amplitude = 10.0 / 1000.0 / sqrt(2) / num_carrier_freqs_for_this_oscillator
    control_inits = []
    for iosc in range(nqubits):
        nfreqs = len(setup.carrier_frequencies[iosc])
        amp = 10.0 / 1000.0 / np.sqrt(2) / nfreqs
        init = ControlInitializationSettings()
        init.init_type = ControlInitializationType.RANDOM
        init.amplitude = amp
        control_inits.append(init)
    setup.control_initializations = control_inits

    results = optimize(
        setup,
        target=unitary,
        quiet=True,
    )

    assert_results_equal(
        t=results.time,
        p_samples=results.p_samples,
        q_samples=results.q_samples,
        infidelity=results.infidelity,
        energy=results.expected_energy,
        population=results.population,
        T=T,
        n_osc=n_osc,
        n_levels=n_levels,
        expected_length=EXPECTED_LENGTH,
        expected_infidelity=EXPECTED_INFIDELITY,
        expected_pt=EXPECTED_PT,
        expected_qt=EXPECTED_QT,
        expected_energy=EXPECTED_ENERGY,
        expected_population=EXPECTED_POPULATION,
        sample_indices=SAMPLE_INDICES,
    )
